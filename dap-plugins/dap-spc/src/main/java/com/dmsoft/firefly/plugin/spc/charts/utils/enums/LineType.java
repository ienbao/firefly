/**
 * Defines what to show the line shape.
 */
public enum LineType {

    /**
     * A solid line is a line which is made of a series of continuous dots
     */
    SOLID,

    /**
     * A dashed line is a line which is made of a series of noncontinuous dots
     */
    DASHED,

    /**
     * A dotted line is a line which is made of a row of dots
     */
    DOTTED,

    /**
     * A double line is line which is made of double line
     */
    DOUBLE
}
