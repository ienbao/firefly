package com.dmsoft.firefly.plugin.spc.utils;

/**
 * xy data for chart data utils
 *
 * @author Can Guan
 */
public class XYData {
    private Double[] x;
    private Double[] y;

    public Double[] getX() {
        return x;
    }

    public void setX(Double[] x) {
        this.x = x;
    }

    public Double[] getY() {
        return y;
    }

    public void setY(Double[] y) {
        this.y = y;
    }
}
